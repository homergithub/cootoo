package com.cootoo.common.exception;

public class SexColumnFormatException extends DataFormatException{

	private static final long serialVersionUID = -3660061932541310437L;

	public SexColumnFormatException() {
		super();
	}

	public SexColumnFormatException(String message, Throwable cause,
			boolean enableSuppression, boolean writableStackTrace) {
		super(message, cause, enableSuppression, writableStackTrace);
	}

	public SexColumnFormatException(String message, Throwable cause) {
		super(message, cause);
	}

	public SexColumnFormatException(String message) {
		super(message);
	}

	public SexColumnFormatException(Throwable cause) {
		super(cause);
	}

	
	
}
